//for loop

for (let step = 0; step < 5; step++) {
  // Runs 5 times, with values of step 0 through 4.
  console.log("Walking east one step");
}

//while loop
let studentNumber = 0;
while(studentNumber < 5) {
  console.log("There are less than 5 Students");
  studentNumber++;
}

//do while
let i = 0;
do {
  console.log("Iteration " + i);
  i++;
} while (i < 5);